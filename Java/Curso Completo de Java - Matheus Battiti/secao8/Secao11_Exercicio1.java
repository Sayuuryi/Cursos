package secao11;

public class Secao11_Exercicio1 {

    public static void main(String[] args) {
        
    
        // Somatorio com FOR
        int somatorio = 0;

        for(int i = 1; i <= 100; i++) {

            somatorio += i;

        }

        System.out.println("Soma de 1 a 100: " + somatorio);
    
    }

}