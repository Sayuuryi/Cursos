package secao11;

public class Secao11_Exercicio2 {

    public static void main(String[] args) {
        
        int numero = 1;
        
        // While verificando se o numero é menor que 20
        while (numero <= 20) {

            if (numero % 2 == 0) {
                System.out.println("O Numero " + numero + " é PAR"); 
        
            }
        
        numero ++;

        }

    }

}